package e;

public class Magician {
	
	String name;
	int healthPoint;
	int attackDamage;
	int abilityPower;
	int defense;
	int magicResistance;
	
	public Magician(String a, int hp, int ad, int ap, int def, int mr) {
		name = a;
		healthPoint = hp;
		attackDamage = ad;
		abilityPower = ap;
		defense = def;
		magicResistance = mr;
	}
	

}
